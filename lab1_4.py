# Варіант №9
#
#   Вводиться додатне ціле трицифрове число.
#   Знайти суму цифр числа. Операціями ділення націл
#   і остача від ділення користуватися не можна.
# 
# Дзюба Владислав

a = input("Введіть додатне ціле трицифрове число: ")

numbers = list(map(int, list(a)))

suma = numbers[0] + numbers[1] + numbers[2]

print("Сума цифр числа:", suma)
